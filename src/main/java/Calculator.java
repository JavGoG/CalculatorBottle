public class Calculator {
    public int add (int i, int n){
        int result = i + n;
        return result;
    }
    public int sustract (int i, int n){
        int result = i - n;
        return result;
    }
    public int multiply (int i, int n){
        int result = i * n;
        return result;
    }
    public double divide (double i, double n){
        double result = i / n;
        return result;
    }
}
